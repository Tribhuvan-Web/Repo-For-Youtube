// Write a java program to find the subtract of two numbers;
class program {
    public static void main(String[] args) {
        int b = 10;
        int a = 2;
        // Using third variable
        int c = b - a;
        System.out.println("Divide of the number is " + c);
        // Without using third variable
        System.out.println("Divide of the number is " + (b / a));
    }
}