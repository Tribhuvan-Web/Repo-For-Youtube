// Write a java program to find the subtract of two numbers;
class program {
    public static void main(String[] args) {
        int b = 98;
        int a = 56;
        // Using third variable
        int c = b - a;
        System.out.println("Subtract of the number is " + c);
        // Without using third variable
        System.out.println("Subtract of the number is " + (b - a));
    }
}